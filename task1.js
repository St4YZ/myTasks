const myDecFunction = function (a,b) {
    if(a > b){
        return a
    }
    else
    {
        return b
    }
}
console.log(myDecFunction(10,20))
function myExpFunction (c,d) {
    if(c > d){
        return c
    }
    else
    {
        return d
    }
}
console.log(myExpFunction(30,15))


const myArrowFunction = (e,f) => {
    if(e > f){
        return e
    }
    else
    {
        return f
    }
}
console.log(myArrowFunction(9,8))


