const myDecFunction = function(a,b) {
    a > b ? console.log(a) : console.log(b)
}
myDecFunction(20,40)

function myExpFunction (c,d) {
    c > d ? console.log(c) : console.log(d)
}
myExpFunction(30,15)


const myArrowFunction = (e,f) =>  e > f ? console.log(e) : console.log(f)
myArrowFunction(9,15)


